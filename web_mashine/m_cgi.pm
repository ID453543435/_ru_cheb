    use CGI; # load CGI routines
    use strict;
#------------------------------------------------------
    package m_cgi;

    use vars qw($cgi);
#------------------------------------------------------
# null
#------------------------------------------------------
    sub null {
        my ($par)=@_;


        return $par;
    }
#------------------------------------------------------
# header
#------------------------------------------------------
sub header {

    $cgi->start_html('mserv'), # start the HTML
    $cgi->h1('mserv'), # level 1 header

    print "<!---beg---!>\n";


    return;
}
#------------------------------------------------------
# fin
#------------------------------------------------------
sub fin {

    print "<!---end---!>\n";
    $cgi->end_html; # end the HTML

    return;
}
#------------------------------------------------------
# init
#------------------------------------------------------
sub init {

    $cgi = CGI->new; # create new CGI object
    print $cgi->header, # create the HTTP header

    header();

    return;
}
